function toggleMenu() {
    var mobileSubMenu = document.getElementById("mobileSubMenu");
    if (mobileSubMenu.style.display === "none") {
        mobileSubMenu.style.display = "block";
    } else {
        mobileSubMenu.style.display = "none";
    }
}


